            <h2 class="text-center mt-3 pt-3" tabindex="0">Doutor Sono (2019): Continuação de "O Iluminado"</h2>

            <div class="conteiner">
                <div class="row">
                    <div class="col-sm-10" style="margin: 30px auto 30px auto;">
                        <p tabindex="0">“O Iluminado” foi um filme que gerou reações mistas ao ser lançado. A adaptação de um dos livros mais populares de Stephen King desagradou tanto o autor, quanto seus fãs. Com mudanças significativas no desenvolvimento das personagens, na condução da história e, especialmente, no seu final, King sempre defendeu que ao longa faltou boa parte da essência do livro. E, eis que quase quatro décadas depois, uma sequência é feita para o livro e ela também ganha uma adaptação ao cinema. Mas afinal, “Doutor Sono”, adapta o livro ou continua a história do filme? De certa forma, as duas coisas.</p>

                        <p tabindex="0">A história se passa cerca de 40 anos depois de “O Iluminado”, com Danny – agora Dan – (Ewan McGregor) já sabendo lidar com alguns fantasmas do passado, mas não o mais relevante de todos. Ele, a exemplo de seu pai, tornou-se um alcoólatra, que busca por algum tipo de redenção. Enquanto isso, um grupo de “vampiros” que se alimentam dos poderes de pessoas iluminadas descobrem uma garota chamada Abra (Kyliegh Curran) que tem poder suficiente para saciá-los por muito tempo.</p>

                        <p tabindex="0">“Doutor Sono” é uma produção complicada pela sua proposta. Seu material de origem ignora a existência do filme lançado em 1980, criando um desafio para que a sequência consiga lidar com seus acontecimentos. Felizmente, o longa tem um roteiro eficiente, que consegue continuar o trabalho de Stanley Kubrick, sem abandonar o livro de Stephen King. Paradoxalmente ele ainda consegue reaproveitar elementos de “O Iluminado” para fazer as pazes com o autor.</p>

                        <p tabindex="0">A Mike Flanagan (“Jogo Perigoso”), diretor e roteirista, coube a missão ingrata de lidar com a ambiguidade que o filme pedia. E é irônico pensar que, narrativamente, é na conexão mais direta com o seu antecessor que reside o principal problema desta adaptação. Embora as homenagens sejam visualmente belas, o ato final é desnecessariamente longo, e poderia ter sido resolvido de modo mais ágil. Certamente, 20 minutos a menos não fariam falta aqui, deixariam a obra com um ritmo mais agradável e poderia trazer um final para o Dan mais próximo ao do livro, fazendo uma ponte com seu desvio de caráter nos primeiros minutos e evidenciando a sua redenção.</p>

                        <p tabindex="0">Contudo, todo o restante do filme possui um ritmo agradável, com a direção sabendo conduzir os momentos de terror, e trazendo uma forte carga emocional. Isso enriquece a narrativa, pois ela não precisa trabalhar o medo a cada cena. São momentos de respiro, que humanizam Dan ao trabalhar suas fraquezas, e potencializam o terror.</p>

                        <p tabindex="0">E tudo isso funciona muito bem graças a atuação de McGregor. Em excelente forma, o ator só precisa de um olhar para demonstrar seus sentimentos, carregando com perfeição alguns dos momentos mais cruciais do longa. Mas quem realmente brilha aqui (com o perdão do trocadilho que se perde na tradução) é Rebecca Ferguson. A atriz desempenha seu papel de vilã com sutis toques de malícia e diversão. Um tempo de tela maior para ela poderia desenvolver ainda mais uma personagem tão rica quanto Rose. Mas, sua desenvoltura ao longo do filme faz deste um de seus melhores trabalhos.</p>

                        <p tabindex="0" style="padding-bottom: 40px;">“Doutor Sono” é um terror sobre fazer as pazes com o passado, e que de modo muito peculiar, funciona tanto para o Dan quanto para Stephen King. É sobre lidar com os fantasmas que passaram a vida nos assombrando e viver sem ter medo deles. Dan(ny) sempre foi um personagem que tinha muito para ser explorado, desde “O Iluminado”. É agradável e terrível (ambas as coisas no melhor sentido) ver como ele cresceu e como lida com seus traumas. Ver que se tornou uma versão piorada de seu pai, e como ele decide lidar com isso. A sua própria maneira, este filme acabou se tornando a melhor sequência que um clássico (da literatura e do cinema) poderia receber.</p>

