                    <h2 class="blog-post-title" tabindex="0">Toy Story 4: Uma nova aventura de brinquedo</h2>
                    <p class="text-muted" tabindex="0">A nova aventura de Woody pelo criativo universo criado pela parceria entre Disney e Pixar ganha ar autêntico, ainda que um pouco pálido diante dos filmes anteriores</p>      
                    
                    <blockquote tabindex="0">
                            <p class="blog-post-meta">O texto abaixo foi escrito por Denis Le Senechal Klimiuc do portal Cinema com Rapadura.</p>
                    </blockquote>
                    
                    <hr>
                    
                    <p tabindex="0">Quando “Toy Story 3” chegou aos cinemas em 2010, o mundo se despediu de uma das grandes sagas criadas para as gerações. Uma história que começou em 1995 e que, com o passar dos anos, ganhou força no imaginário popular, convencendo crianças, adolescentes e adultos de que brinquedos têm, de fato, vida própria. Agora, com “Toy Story 4”, o estreante Josh Cooley leva os espectadores a mais uma grande aventura.</p>
                    <p tabindex="0">Após ganharem um novo lar no último episódio, a trupe liderada por Woody (Tom Hanks, “The Post: A Guerra Secreta”) aproveita ao máximo a infância de Bonnie (Madeleine McGraw, “A Maldição da Chorona”). Acompanhado de Buzz Lightyear (Tim Allen, “Minhas Seis Esposas”), seu grande amigo de aventuras, o veterano cowboy se vê em um grande dilema: ele quer passar todo o tempo com a nova criança, mas sente muita falta de Andy (John Morris, “Toy Story”), seu amigo anterior.</p>
                    <p tabindex="0">Apesar do grande dilema, Woody continua firme e forte em sua grande qualidade: a lealdade que tem à sua dona e aos amigos brinquedos. Ao se ver preocupado com a mudança que Bonnie sofrerá ao entrar em uma nova etapa de sua vida escolar, ele se engaja em ajudá-la, escondido em sua mochila, em seu primeiro dia de aula. E é justamente nesta data tão especial que a pequena cria um novo brinquedo a pedido de sua professora: o Garfinho (Tony Hale, “Com Amor, Simon”). Porém, para a surpresa de Woody, o novo brinquedo não se enxerga como tal, buscando constantemente o seu lugar por natureza, o lixo. O vaqueiro desengonçado parte então em um trabalho gigantesco de convencimento, enquanto ele e sua trupe de brinquedos acompanham Bonnie e seus pais em uma viagem pelo país.</p>
                    <p tabindex="0">É claro que as coisas dão errado. E é claro que Woody se mete em uma aventura ainda maior, precisando resolver novas e velhas pendências com brinquedos atuais e outros nem tanto. Assim, “Toy Story 4” se torna um novo e exemplar capítulo da saga dos brinquedos que a Disney e a Pixar constroem com tanto carinho. Agora, porém, o tom de despedida vivenciado no terceiro filme já não é tão agudo, o que pode criar a estranha sensação de anticlímax.</p>
                    <p tabindex="0">Apesar de o espectador acompanhar uma aventura já sem o mesmo tom de novidade, é notável o esforço do diretor e de seus roteiristas, Andrew Stanton (“Wall-E”) e Stephany Folsom (da série “Star Wars: Resistance”), em trazer elementos novos e atraentes a este universo já tão conhecido. Ao mesmo tempo, a tonalidade nostálgica continua firme, agora com o retorno de Betty (Annie Potts, “Caça-Fantasmas”), a boneca pastora pela qual Woody sempre nutriu uma paixão e que foi doada há muitos anos.</p>
                    <p tabindex="0">Os elementos novos, por sua vez, funcionam por trazer um ar fresco à franquia. Há um tom vilanesco na abandonada Gabby Gabby (Christina Hendricks, “Doce Argumento”), além dos impagáveis Ducky (Keegan-Michael Key, “O Rei Leão”) e Bunny (Jordan Peele, da série “The Twilight Zone”). E cada elemento funciona, pois o roteiro é comedido a ponto de não deixá-los sobressair além dos protagonistas.</p>
                    <p style="padding-bottom: 30px;" tabindex="0">Aliás, o protagonismo de Woody neste capítulo é forte o suficiente para ter seus companheiros brinquedos apenas como parte do cenário, menos do que coadjuvantes. Desta forma, o espectador pode sentir falta dos elementos que tanto roubaram a cena nos três capítulos anteriores, em prol de novos personagens, o que tira parte da identidade de “Toy Story“. Por isso, apesar de ser um capítulo à parte da grande despedida do terceiro filme, este “Toy Story 4” diverte e emociona, mas fica aquém de tudo o que a saga apresentou até agora.</p>
