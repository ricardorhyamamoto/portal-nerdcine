                    <h2 class="blog-post-title" tabindex="0">O Exterminador do Futuro: Destino Sombrio</h2>
                    <p class="text-muted" tabindex="0">Mesmo sem Skynet, o futuro da série ainda não está garantido</p>      
                    
                    <blockquote tabindex="0">
                            <p class="blog-post-meta">O texto abaixo foi escrito por Marcelo Forlani do portal Omelete.</p>
