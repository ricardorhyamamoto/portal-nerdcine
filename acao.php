                    <hr>
                    
                    <p tabindex="0">O novo filme da franquia Exterminador do Futuro tenta nos fazer esquecer que existiram Exterminador do Futuro 3: A Rebelião das Máquinas (2003), O Exterminador do Futuro: A Salvação (2009) e O Exterminador do Futuro: Gênesis (2015) e cria uma nova continuação imediata para seu melhor produto: O Exterminador do Futuro 2 - O Julgamento Final (1991), o último da série que foi dirigida pelo seu criador, James Cameron.</p>
                    <p tabindex="0">Para dar mais legitimidade, o próprio diretor de Titanic e Avatar retoma para si o controle, mas como produtor-executivo, e chama Tim Miller, do divertido Deadpool, para dirigir a aventura. Segundo a campanha promocional do filme, esta é a sequência que realmente vale. A ideia é ótima e a nova aventura começa se utilizando de imagens filmadas na época do segundo longa-metragem. Logo em seguida, usando a tecnologia que temos disponível hoje, vem a cena mais impressionante: o reencontro de Sarah Connor, o adolescente John Connor e o T-800, recriados por computação gráfica para que os dublês Jessi Fisher, Brett Azar e Jude Collie emprestassem seus corpos e movimentos para recriar os visuais de Linda Hamilton, Edward Furlong e Arnold Schwarzenegger de 1991! O que acontece na cena, muda o futuro como o conhecíamos, criando, assim, uma nova linha temporal, sem Skynet, mas com outros problemas para Sarah Connor.</p>
                    <p tabindex="0">Se nos primeiros dois filmes, ela vai de donzela em perigo a marombeira que arrisca tudo para proteger seu filho, agora ela é mais protagonista do que nunca. Carregando um armamento pesado, ela está sempre presente quando um novo ser chega do futuro. E se antes as máquinas da Skynet pareciam bem calibradas, os novos viajantes no tempo sofrem com a forma como são depositados neste novo presente.</p>
                    <p tabindex="0">Os novos personagens na história são Grace (Mackenzie Davis), Dani (Natalia Reyes) e o novo vilão, Rev-9 (Gabriel Luna). Grace é uma humana com melhorias, tipo um Robocop (ou, se você for antigo como eu, uma Mulher Biônica, versão feminina do Homem de Seis Milhões de Dólares). Dani é o alvo da vez. E Rev-9 é tipo o T-1000, mas trocando o metal cromado por uma gosma preta tipo Venom e um endoesqueleto que consegue se separar, dividindo-se em duas unidades quando a necessidade aparece.</p>
                    <p tabindex="0">O filme de 1991 foi inovador como nenhum da franquia conseguiu ser nos últimos 28 anos, criou frases marcantes ("I’ll be back”, “Hasta la vista, baby”), trouxe um vilão como nunca havíamos visto antes, o indestrutível T-1000, e ainda trazia uma reviravolta, que transformou o T-800 interpretado por Arnold Schwarzenegger de perseguidor no primeiro filme a salvador no segundo. Já esta sequência é uma aventura que se espelha no original em forma e tenta ganhar o jogo na ação. Boas cenas de ação, com computação gráfica de ponta, mas sem originalidade. Vemos perseguições em autoestradas seguidas de combates e tiros de bazuca; pancadaria em locações industriais cheias de concreto, ferro e calor; luta em um avião em queda. Sim, já vimos tudo isso sendo feito antes em produções como Matrix, Missão: Impossível, Mad Max, 007 e até no próprio Exterminador do Futuro. E o que é pior, sem que tudo isso leve para algum lugar.</p>
                    <p tabindex="0">O que diferencia os filmes de James Cameron é que ele é um ótimo contador de histórias. Ele nos fez parar mais de duas horas vendo um navio que a gente já sabia que ia afundar. Avatar é um Pocahontas com personagens grandes e azuis, uma história que já conhecíamos, mas estava embalada de um jeito completamente novo, diferente e envolvente. Tim Miller não tem este dom. Pelo menos por enquanto. Mas a culpa não é só dele.</p>
                    <p style="padding-bottom: 30px;" tabindex="0">O roteiro de David Goyer, Justin Rhodes e Billy Ray parece apenas seguir uma lista de itens obrigatórios de filmes de ação. Tanto é que tenta criar um elemento “girl power” em Dani, provavelmente na esteira de sucessos recentes de Mad Max: Estrada da Fúria, Mulher-Maravilha e Capitã Marvel. Mas acaba soando falso. É uma pena, pois os atores estão ótimos. É maravilhoso ver Linda Hamilton chutando bundas ao lado de Mackenzie Davis e Schwarzenegger não se levando a sério, mas sempre fica aquele gosto de promessa não cumprida. Esta ainda não é a linha do tempo em que temos uma sequência à altura de Exterminador do Futuro 2. E tinha gente que pensava que o problema era a Skynet…</p>
